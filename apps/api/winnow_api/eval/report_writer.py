"""Serialize an EvalReport to the two committed artifacts.

- ``apps/site/app/evals/results.json`` — imported by the /evals page at
  build time so the site shows real numbers, not hardcoded ones.
- ``docs/evals.md`` — human-readable, linked from the README and the
  ``users.confidence_threshold`` code comment (#threshold-selection).

Both are regenerated by ``winnow eval`` and committed to the repo.
"""

from __future__ import annotations

import json
from pathlib import Path

from winnow_api.eval.harness import EvalReport

_REPO_ROOT = Path(__file__).resolve().parents[4]
RESULTS_JSON = _REPO_ROOT / "apps" / "site" / "app" / "evals" / "results.json"
EVALS_MD = _REPO_ROOT / "docs" / "evals.md"

_STRATEGY_LABEL = {
    "pure_classifier": "Pure classifier (tier-1 only)",
    "pure_llm": "Pure LLM (tier-2 only)",
    "tiered": "Tiered (Winnow)",
}


def write_results_json(report: EvalReport, path: Path = RESULTS_JSON) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(report.to_dict(), indent=2), encoding="utf-8")


def _fmt_pct(x: float) -> str:
    return f"{x * 100:.1f}%"


def _fmt_ms(x: float) -> str:
    return f"{x:.1f} ms" if x < 1000 else f"{x / 1000:.2f} s"


def _fmt_usd(x: float) -> str:
    return f"${x:.4f}"


def _fmt_threshold(x: float) -> str:
    """Enough precision to distinguish 0.995 / 0.999 / 0.9999 — a flat
    .2f collapses them all to 0.99/1.00 and the sweep looks duplicated."""
    return f"{x:.4f}".rstrip("0").rstrip(".")


def render_markdown(report: EvalReport) -> str:
    d = report.to_dict()
    lines: list[str] = []
    lines.append("# Winnow evals")
    lines.append("")
    lines.append(
        f"_Generated {d['generated_at']} · held-out test set: {d['n_test']} emails "
        f"({int(d['test_fraction'] * 100)}% of the synthetic corpus, seed {d['seed']}) · "
        f"tiered threshold {d['threshold']}._"
    )
    lines.append("")

    if d["tier_2_provenance"] == "stub":
        lines.append(
            "> ⚠️ **Tier-2 is stubbed in this run.** The LLM fixtures are "
            "rule-based placeholders whose lanes mirror ground truth, so the "
            "**accuracy** columns for _Pure LLM_ and _Tiered_ are not "
            "meaningful. **Latency and cost are modeled** from token counts at "
            "Claude Opus pricing. Classifier accuracy, escalation rate, and all "
            "timing/cost figures are real."
        )
        lines.append("")

    # --- headline comparison table ---
    lines.append("## Strategy comparison")
    lines.append("")
    lines.append(
        "| Strategy | Accuracy | Macro-F1 | Mean latency | p95 latency | "
        "Cost / 1000 | Escalated |"
    )
    lines.append("|---|---|---|---|---|---|---|")
    for name in ("pure_classifier", "pure_llm", "tiered"):
        m = d["strategies"][name]
        lines.append(
            f"| {_STRATEGY_LABEL[name]} | {_fmt_pct(m['accuracy'])} | "
            f"{m['macro_f1']:.3f} | {_fmt_ms(m['mean_latency_ms'])} | "
            f"{_fmt_ms(m['p95_latency_ms'])} | {_fmt_usd(m['cost_per_1000_usd'])} | "
            f"{_fmt_pct(m['escalation_rate'])} |"
        )
    lines.append("")

    # --- threshold sweep ---
    lines.append("## Threshold selection")
    lines.append("")
    lines.append(
        "How the tiered strategy behaves as the tier-1 confidence threshold "
        "moves. Higher threshold → more emails escalate to the LLM → higher "
        "cost and latency. This is the sweep behind the default "
        "`confidence_threshold`."
    )
    lines.append("")
    lines.append(
        "| Threshold | Escalated | Accuracy | Macro-F1 | Cost / 1000 | Mean latency |"
    )
    lines.append("|---|---|---|---|---|---|")
    for p in d["threshold_sweep"]:
        lines.append(
            f"| {_fmt_threshold(p['threshold'])} | {_fmt_pct(p['escalation_rate'])} | "
            f"{_fmt_pct(p['accuracy'])} | {p['macro_f1']:.3f} | "
            f"{_fmt_usd(p['cost_per_1000_usd'])} | {_fmt_ms(p['mean_latency_ms'])} |"
        )
    lines.append("")

    # --- per-lane breakdown for the tiered strategy ---
    lines.append("## Per-lane breakdown (tiered)")
    lines.append("")
    lines.append("| Lane | Precision | Recall | F1 | Support |")
    lines.append("|---|---|---|---|---|")
    for lane, lm in d["strategies"]["tiered"]["per_lane"].items():
        lines.append(
            f"| {lane} | {lm['precision']:.3f} | {lm['recall']:.3f} | "
            f"{lm['f1']:.3f} | {lm['support']} |"
        )
    lines.append("")

    lines.append("## Notes")
    lines.append("")
    for note in d["notes"]:
        lines.append(f"- {note}")
    lines.append("")
    lines.append(
        "_Regenerate with `winnow eval`. Numbers are reproducible from the "
        "seeded split._"
    )
    lines.append("")
    return "\n".join(lines)


def write_markdown(report: EvalReport, path: Path = EVALS_MD) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(render_markdown(report), encoding="utf-8")


def write_all(report: EvalReport) -> tuple[Path, Path]:
    write_results_json(report)
    write_markdown(report)
    return RESULTS_JSON, EVALS_MD
